package auto;

public class Koleso {

	private int radius;

	 

	public Koleso(int radius) {
		super();
		this.radius = radius;
	}

	public int reSizingKoleso(int reSizingKoleso) {
		radius = reSizingKoleso;
		return radius;
	}

	public String toString() {
		return "Koleso [radius=" + radius + "]";
	}

}
